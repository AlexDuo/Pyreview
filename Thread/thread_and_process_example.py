# 一个应用程序会调用许多计算资源和硬件资源，但是一个应用程序要以一个整体的形式暴露给操作系统，就像被包装起来一样，这个包装里面
# 包括对各种资源的调用，内存的管理，网络接口的调用等。这个包装就是对各种资源管理的集合，这个集合就是进程.

# 进程要操作CPU，必须要创建一个线程，进程本身不会执行，进程只是资源的集合。

