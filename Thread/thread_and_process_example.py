# Thread 线程是操作系统能够进行运算调度的最小单位。它被包含在进程之中，是进程中实际运作的单位。一条线程指的是进程中一个单一顺序
# 的控制流，一个进程中可以并发多个线程，每条线程并行执行不同的任务。我们可以理解为线程是一系列指令的集合。当OS需要cpu进行计算的时候
# os调度cpu最小的单位就是线程
#
# 每一个程序的内存空间都是独立的无法互相访问
